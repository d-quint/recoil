const CHAR_W = 4; // 3px glyph + 1px gap

/**
 * Draw text at (x, y) using the bitmap font.
 * @param {CanvasRenderingContext2D} ctx
 * @param {string} text
 * @param {number} x
 * @param {number} y
 * @param {"left"|"center"|"right"} [align="left"]
 */
export function drawText(ctx, text, x, y, align = "left", color = FG) {
  text = text.toUpperCase();
  const totalW = text.length * CHAR_W - 1;

  let startX = x;
  if (align === "center") startX = x - totalW / 2;
  else if (align === "right") startX = x - totalW;

  ctx.fillStyle = color;
  for (let i = 0; i < text.length; i++) {
    const glyph = GLYPHS[text[i]];
    if (!glyph) continue;
    const gx = startX + i * CHAR_W;
    for (let row = 0; row < 5; row++) {
      for (let col = 0; col < 3; col++) {
        if (glyph[row][col] === "1") {
          ctx.fillRect((gx + col) | 0, (y + row) | 0, 1, 1);
        }
      }
    }
  }
}
